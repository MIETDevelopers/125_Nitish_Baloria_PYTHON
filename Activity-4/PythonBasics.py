# Built-in functions

# abs() function
x=abs(-7.25)
print(x)

# len() function
x=len("Hello")
print(x)

# min() function
x=min(5,10)
print(x)

# max() function
x=max(20,25)
print(x)

# round() function
x=round(5.86)
print(x)

# isalnum() function
txt="Company12"
x=txt.isalnum()
print(x)

# type() function
a=5
print(a, "is of type", type(a))

b=4.0
print(b, "is of type", type(b))

c=3+6j
print(c, "is of type", type(c))
